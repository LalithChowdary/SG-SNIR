"""
experiments/exp8_robustness.py
================================
Experiment 8 — Robustness to Seed Selection

Claim: SG-SNIR performs consistently across different initial infection
configurations.

Setup:
  - Fix k = 10
  - Seed sizes: 1, 3, 5, 10, 20
  - 10 trials per seed size (numpy seed 42 → deterministic per-trial seeds)
  - Record H(e) after k removals for SG-SNIR and MaxExpectedH

Output: results/experiment8/<dataset>_robustness.json
        (box plots generated by plot_results.py)

Usage
-----
  python -m experiments.exp8_robustness
  python -m experiments.exp8_robustness --datasets HIV
  python -m experiments.exp8_robustness --skip-maxh
"""

import argparse
import json
import math
import time
from pathlib import Path
from typing import List

import numpy as np

from src.data_loader import load_dataset, seed_fixed_nodes, assign_fixed_weights
from src.snir_model import SNIRParams
from src.sg_snir import sg_snir_blocking
from src.baselines import max_expected_h_blocking

RESULTS_DIR = Path("results/experiment8")
RESULTS_DIR.mkdir(parents=True, exist_ok=True)

DATASETS     = ["HIV", "Wiki-Vote"]
K_FIXED      = 10
T_HORIZON    = 10
BASE_SEED    = 42
BASE_PARAMS  = SNIRParams(alpha=0.033, beta=0.022, delta=0.020,
                           eta=0.140, gamma=0.315, xi=0.300)

SEED_SIZES   = [1, 3, 5, 10, 20]
TRIALS       = 10    # 10 trials per seed size — consistent with plan Section 3.3


def run_experiment8(
    datasets: List[str] = DATASETS,
    run_maxh: bool = True,
    verbose: bool = False,
):
    print("\n" + "="*70)
    print("  EXPERIMENT 8 — Robustness to Seed Selection")
    print("="*70)

    # Generate all trial seeds deterministically from BASE_SEED
    rng = np.random.default_rng(BASE_SEED)
    # Shape: (n_seed_sizes, TRIALS) — unique seed per (seed_size_idx, trial_idx)
    trial_seeds = rng.integers(0, 100_000, size=(len(SEED_SIZES), TRIALS)).tolist()

    for dataset in datasets:
        print(f"\n{'─'*60}\n  Dataset: {dataset}")
        G_orig = load_dataset(dataset)
        assign_fixed_weights(G_orig)

        dataset_result = {
            "dataset": dataset,
            "k": K_FIXED,
            "seed_sizes": SEED_SIZES,
            "n_trials": TRIALS,
            "results": {},
        }

        for s_idx, n_seeds in enumerate(SEED_SIZES):
            print(f"\n  Seed size = {n_seeds} ({TRIALS} trials)")
            sg_H_finals, meh_H_finals = [], []

            for trial in range(TRIALS):
                trial_seed = trial_seeds[s_idx][trial]
                np.random.seed(trial_seed)

                initial_S, initial_N, initial_I, initial_R = seed_fixed_nodes(
                    G_orig, n_infected=n_seeds, seed=int(trial_seed)
                )

                # SG-SNIR
                _, sg_H, _ = sg_snir_blocking(
                    G_orig, initial_S, initial_N, initial_I, initial_R,
                    BASE_PARAMS, K_FIXED, T_HORIZON,
                )
                sg_H_finals.append(sg_H[-1] if sg_H else float("nan"))

                # MaxExpectedH
                if run_maxh:
                    _, meh_H, _ = max_expected_h_blocking(
                        G_orig, initial_S, initial_N, initial_I, initial_R,
                        BASE_PARAMS, K_FIXED, T_HORIZON,
                    )
                    meh_H_finals.append(meh_H[-1] if meh_H else float("nan"))

                if verbose:
                    sg_val  = sg_H_finals[-1]
                    meh_val = meh_H_finals[-1] if run_maxh else float("nan")
                    print(f"    trial {trial+1}: SG={sg_val:.4f}  MEH={meh_val:.4f}")

            # Drop NaN entries (occur when sg_snir_blocking / max_expected_h_blocking
            # return an empty H_history, e.g. Gamma(W) is empty from the first iter).
            sg_arr  = [x for x in sg_H_finals  if not math.isnan(x)]
            meh_arr = [x for x in meh_H_finals if not math.isnan(x)] if run_maxh else []

            dataset_result["results"][str(n_seeds)] = {
                "sg_snir": {
                    "H_finals": sg_H_finals,
                    "mean":     round(float(np.mean(sg_arr)),  4) if sg_arr  else None,
                    "std":      round(float(np.std(sg_arr)),   4) if sg_arr  else None,
                    "min":      round(float(np.min(sg_arr)),   4) if sg_arr  else None,
                    "max":      round(float(np.max(sg_arr)),   4) if sg_arr  else None,
                },
                "max_expected_h": {
                    "H_finals": meh_H_finals if run_maxh else [],
                    "mean":     round(float(np.mean(meh_arr)), 4) if meh_arr else None,
                    "std":      round(float(np.std(meh_arr)),  4) if meh_arr else None,
                    "min":      round(float(np.min(meh_arr)),  4) if meh_arr else None,
                    "max":      round(float(np.max(meh_arr)),  4) if meh_arr else None,
                },
            }
            print(f"    SG-SNIR  mean={np.mean(sg_arr):.4f}  std={np.std(sg_arr):.4f}")
            if run_maxh and meh_arr:
                print(f"    MaxExpH  mean={np.mean(meh_arr):.4f}  std={np.std(meh_arr):.4f}")

        out_path = RESULTS_DIR / f"{dataset}_robustness.json"
        with open(out_path, "w") as f:
            json.dump(dataset_result, f, indent=2)
        print(f"\n  ✓ Saved → {out_path}")


def parse_args():
    p = argparse.ArgumentParser()
    p.add_argument("--datasets",   nargs="+", default=DATASETS)
    p.add_argument("--skip-maxh",  action="store_true")
    p.add_argument("--verbose",    action="store_true")
    return p.parse_args()


if __name__ == "__main__":
    args = parse_args()
    run_experiment8(
        datasets=args.datasets,
        run_maxh=not args.skip_maxh,
        verbose=args.verbose,
    )
